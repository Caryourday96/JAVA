   public ArrayList<Insert> getInsertList()
   {
       ArrayList<Insert> InsertList = new ArrayList<Insert>();
       Connection connection = getConnection();
       
       String query = "SELECT * FROM  `users` ";
       Statement st;
       ResultSet rs;
       
       try {
           st = connection.createStatement();
           rs = st.executeQuery(query);

           Insert insert;

           while(rs.next())
           {
        insert = new Insert(rs.getString("Pickup"),rs.getString("Destination"),rs.getString("ID"),rs.getString("Passenger"),rs.getString("Amount"),rs.getString("Acc"),rs.getString("Tel"));
               InsertList.add(insert);
           }

       } 
      catch (Exception e) {
           e.printStackTrace();
       }
       return InsertList;
   }  
   
