   public void Show_Users_In_JTable()
   {
       DefaultTableModel model = (DefaultTableModel)jTable_Display.getModel();
               model.setRowCount(0);
               
               java.util.Date date = new java.util.Date();
        
            SimpleDateFormat Date = new SimpleDateFormat("yyyy-MM-dd");
            String dateOfTrip = Date.format(date);
    
    list = new SQL().getTrips("WHERE date= '"+dateOfTrip+"'");
       
       Object[] row = new Object[8];
       for (int i = 0; i < list.size(); i++ )
       {
           row[0] = list.get(i).getTime();
           row[1] = list.get(i).getPickup();
           row[2] = list.get(i).getDestination();
           row[3] = list.get(i).getDriver_id();
           row[4] = list.get(i).getPassenger_name();
           row[5] = list.get(i).getAmount();
           row[6] = list.get(i).getAccount();
           row[7] = list.get(i).getTelephone();
           
           model.addRow(row);
       }
   }
    
// Execute The Insert Update And Delete Querys
   public void executeSQlQuery(String query, String message)
   {
       Connection con = getConnection();
       Statement st;
       try{
           st = con.createStatement();
           if((st.executeUpdate(query)) == 1)
           {
               // refresh jtable data
               DefaultTableModel model = (DefaultTableModel)jTable_Display.getModel();
               model.setRowCount(0);
               Show_Users_In_JTable();
               
               JOptionPane.showMessageDialog(null, "Data "+message+" Succefully");
           }else{
               JOptionPane.showMessageDialog(null, "Data Not "+message);
           }
       }catch(Exception ex){
           ex.printStackTrace();
       }
   }
   
